#DECLARAÇÃO DA FUNÇÃO
def soma(a, b):
    soma = a + b
    print(f'Resultado: {soma}')

#CHAMADA DA FUNCAO
soma(4, 8)

def subtracao(a, b):
    subtracao = a - b
    return subtracao

#COLOCANDO O RETORNO EM UMA VARIAVEL
resultado_subtracao = subtracao(5,2)
print(f'Resultado: {resultado_subtracao}')